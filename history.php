<div  align="left">La bataille pour Armageddon est une bataille entre les Orks et les forces de l'Imperium (a savoir les Space Marines et la Garde imperiale, aillant le plus mobiliser de troupes, en effet, pas moins de douze space hulks et des milliers de vaisseaux Orks furent envoyes, 
et ils furent opposes a une vingtaine de chapitres Space Marines. Les commandants des deux forces en presence furent, pour les Orks : le Seigneur de Guerre Ghazghkull Mag Uruk Thraka, et, pour l'Imperium, le Commissaire Yarrick et le colonel Vladimer Nikita Kurov 
(pour les forces au sol) et l'amiral Parol (pour la flotte).Elle se deroula, en fait, en deux parties :
La premiere partie fut un assaut surprise des Orks qui envahirent le monde d'Armageddon Prime, von Strab, l'ancien gouverneur du systeme, ne fit rien et n'alerta pas l'Imperium; en effet, il pensait que les Orks ne parviendraient jamais a franchir la jungle qui 
les separait d'Armageddon Secundus. Ils le firent quand même, mais subirent quand même quelques pertes, dues a l'envoi par von Strab du regiment de titans de l'Adeptus Mechanicus, mais, malheureusement pour les Orks, pendant qu'ils traversaient la jungle, le 
Commissaire Yarrick avait, contre l'avis de son superieur, alerte l'Imperium, ce qui lui valut une mutation dans la metropole surpeuplee de la ruche Hades. Un jour, ayant enfin reussi a franchir la jungle, les Orks commencerent a assaillir la ruche Helsreach. 
Les batailles furent âpres, mais elle fut finalement vaincue. C'est dans la ruche Hades que les combats furent les plus âpres, en effet, grâce au commandement inspire de Yarrick, les forces Imperiales reussissaient a repousser les Orks vagues apres vagues 
(ou plutot Waaaaagh ! apres Waaaaagh !). Ghasghkull divisa ses troupes pour envahir les ruches Tartarus et Acheron, situees plus a l'est de la planete. Les combats duraient depuis des semaines et aucune des trois ruches n'etait encore tombee, quand les renforts 
arriverent, sous la forme des chapitres Spaces Marines des Salamanders, Ultramarines et Blood Angels. Les bombes lâchees par leurs vaisseaux firent une hecatombe chez les peaux-vertes qui furent forcees de se replier et, apres d'autres combats, les Orks quitterent le systeme.
Les Orks ne partaient pas vraiment, car ils commençaient a se reproduire dans les jungles. Ghazghkull rumina sa defaite pendant cinquante ans, et, apres avoir obtenu l'aide de Nazdreg Ug Gurdub, un seigneur Bad Moonz, et apres que ce dernier 
lui a offert un teletransporteur, le "tela-tepula", que ses mekanos venaient d'inventer. Il revint donc en l'an 998 du 41e millenaire, avec toujours plus d'Orks, il fit tomber les ruches Acheron, Hades, Helsreach et Tempestora.
La guerre sans fin etant un concept ork du paradis, des Orks viennent toujours se mêler aux forces de Ghazghkul, mais les imperiaux tiennent bon.
La bataille pour Armageddon se poursuit encore aujourd'hui.
